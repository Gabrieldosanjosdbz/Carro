package exercicios_array_matriz;

// criarei a classe carro

public class carro {
	
	String nome;
	
	String modelo;
	
	String cor;
	
	public carro() {
		
	}

	public carro(String nome, String modelo, String cor) {
		
		this.nome = nome;
		this.modelo = modelo;
		this.cor = cor;
	}
		
	
	 public String getNome() {
	        return nome;
	    }

	    public String getMarca() {
	        return modelo;
	    }

	    public String getCor() {
	  
	        return cor;
		
		
	}

}
